<template>
  <div>
    <div class="flex flex-col sm:flex-row justify-between items-start sm:items-center mb-6 gap-4">
      <h1 class="text-2xl font-bold">欠席連絡一覧</h1>
      <router-link to="/parent/absences/create">
        <Button variant="success">新規登録</Button>
      </router-link>
    </div>
    
    <!-- 検索フィルター -->
    <div class="bg-white rounded-lg shadow p-4 mb-6">
      <div class="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-4 gap-4">
        <Select
          v-model="filters.division"
          :options="divisionOptions"
          placeholder="区分で絞り込み"
        />
        <Input
          v-model="filters.date_from"
          type="date"
          placeholder="開始日"
        />
        <Input
          v-model="filters.date_to"
          type="date"
          placeholder="終了日"
        />
        <div class="flex gap-2">
          <Button variant="primary" @click="fetchData">検索</Button>
          <Button variant="secondary" @click="resetFilters">クリア</Button>
        </div>
      </div>
    </div>
    
    <!-- データテーブル -->
    <div class="bg-white rounded-lg shadow overflow-hidden">
      <div v-if="loading" class="p-8 text-center text-gray-500">
        読み込み中...
      </div>
      
      <div v-else-if="absences.length === 0" class="p-8 text-center text-gray-500">
        データがありません
      </div>
      
      <div v-else class="overflow-x-auto">
        <table class="min-w-full divide-y divide-gray-200">
          <thead class="bg-gray-50">
            <tr>
              <th class="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">日付</th>
              <th class="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">区分</th>
              <th class="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">理由</th>
              <th class="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">登校予定</th>
              <th class="px-6 py-3 text-right text-xs font-medium text-gray-500 uppercase tracking-wider">操作</th>
            </tr>
          </thead>
          <tbody class="bg-white divide-y divide-gray-200">
            <tr v-for="item in absences" :key="item.id" class="hover:bg-gray-50">
              <td class="px-6 py-4 whitespace-nowrap text-sm">{{ formatDate(item.absence_date) }}</td>
              <td class="px-6 py-4 whitespace-nowrap text-sm">
                <span
                  :class="item.division === '欠席' ? 'bg-red-100 text-red-800' : 'bg-yellow-100 text-yellow-800'"
                  class="px-2 py-1 rounded-full text-xs font-medium"
                >
                  {{ item.division }}
                </span>
              </td>
              <td class="px-6 py-4 text-sm">
                <div class="max-w-xs truncate">{{ item.reason }}</div>
              </td>
              <td class="px-6 py-4 whitespace-nowrap text-sm">{{ item.scheduled_time || '-' }}</td>
              <td class="px-6 py-4 whitespace-nowrap text-right text-sm font-medium">
                <router-link
                  :to="`/parent/absences/${item.id}/edit`"
                  class="text-blue-600 hover:text-blue-900 mr-4"
                >
                  編集
                </router-link>
                <button
                  @click="confirmDelete(item)"
                  class="text-red-600 hover:text-red-900"
                >
                  削除
                </button>
              </td>
            </tr>
          </tbody>
        </table>
      </div>
      
      <!-- ページネーション -->
      <div v-if="pagination.last_page > 1" class="px-6 py-4 border-t border-gray-200">
        <div class="flex justify-between items-center">
          <div class="text-sm text-gray-700">
            全{{ pagination.total }}件中 {{ pagination.from }}-{{ pagination.to }}件を表示
          </div>
          <div class="flex gap-2">
            <Button
              variant="secondary"
              size="sm"
              :disabled="pagination.current_page === 1"
              @click="changePage(pagination.current_page - 1)"
            >
              前へ
            </Button>
            <Button
              variant="secondary"
              size="sm"
              :disabled="pagination.current_page === pagination.last_page"
              @click="changePage(pagination.current_page + 1)"
            >
              次へ
            </Button>
          </div>
        </div>
      </div>
    </div>
    
    <!-- 削除確認モーダル -->
    <Modal
      :show="showDeleteModal"
      title="削除確認"
      @close="showDeleteModal = false"
      @confirm="handleDelete"
    >
      <p>この欠席連絡を削除してもよろしいですか？</p>
      <p class="text-sm text-gray-600 mt-2">{{ formatDate(deleteTarget?.absence_date) }} - {{ deleteTarget?.division }}</p>
      <p class="text-sm text-red-600 mt-2">※ この操作は取り消せません</p>
    </Modal>
  </div>
</template>

<script setup>
import { ref, reactive, onMounted } from 'vue';
import { useParentStore } from '../../../stores/parent';
import Button from '../../../components/Button.vue';
import Input from '../../../components/Input.vue';
import Select from '../../../components/Select.vue';
import Modal from '../../../components/Modal.vue';

const parentStore = useParentStore();

const absences = ref([]);
const loading = ref(false);
const showDeleteModal = ref(false);
const deleteTarget = ref(null);

const filters = reactive({
  division: '',
  date_from: '',
  date_to: ''
});

const pagination = reactive({
  current_page: 1,
  last_page: 1,
  from: 0,
  to: 0,
  total: 0
});

const divisionOptions = [
  { value: '欠席', label: '欠席' },
  { value: '遅刻', label: '遅刻' }
];

const formatDate = (dateString) => {
  if (!dateString) return '-';
  const date = new Date(dateString);
  return `${date.getFullYear()}/${String(date.getMonth() + 1).padStart(2, '0')}/${String(date.getDate()).padStart(2, '0')}`;
};

const fetchData = async (page = 1) => {
  loading.value = true;
  try {
    const params = {
      page,
      division: filters.division || undefined,
      date_from: filters.date_from || undefined,
      date_to: filters.date_to || undefined
    };
    
    const response = await parentStore.fetchAbsences(params);
    absences.value = response.data || response;
    
    if (response.current_page) {
      Object.assign(pagination, {
        current_page: response.current_page,
        last_page: response.last_page,
        from: response.from,
        to: response.to,
        total: response.total
      });
    }
  } catch (error) {
    console.error('データ取得エラー:', error);
  } finally {
    loading.value = false;
  }
};

const resetFilters = () => {
  filters.division = '';
  filters.date_from = '';
  filters.date_to = '';
  fetchData();
};

const changePage = (page) => {
  fetchData(page);
};

const confirmDelete = (item) => {
  deleteTarget.value = item;
  showDeleteModal.value = true;
};

const handleDelete = async () => {
  try {
    await parentStore.deleteAbsence(deleteTarget.value.id);
    showDeleteModal.value = false;
    fetchData(pagination.current_page);
  } catch (error) {
    alert(error.response?.data?.message || '削除に失敗しました');
  }
};

onMounted(() => {
  fetchData();
});
</script>
